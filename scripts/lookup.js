export function lookupDialect(name) {
  return name
    .replace('Bara','bhr')
    .replace('Betsileo','plt')
    .replace('Betsimisaraka Atsimo','bzc')
    .replace('Betsimisaraka Avaratra','bmm')
    .replace('Betsimisaraka','bzc')
    .replace('Bezanozano','plt')
    .replace('Mahafaly','tdx')
    .replace('Makoa','skg')
    .replace('Masikoro','msh')
    .replace('Merina','plt')
    .replace('Provincial','plt')
    .replace('Saint-Marien','bzc')
    .replace(/Sakalava.*/,'skg')
    .replace('Sihanaka','plt')
    .replace('Taifasy','plt')
    .replace('Taimoro','plt')
    .replace('Taisaka','tkg')
    .replace('Tambahoaka','bzc')
    .replace('Tanala','plt')
    .replace('Tandroy','tdx')
    .replace('Tankarana','xmv')
    .replace('Tanosy','txy')
    .replace('Tsimihety','xmw')
    .replace('Vakinankaratra','plt')
    .replace('Vezo','skg')
}





